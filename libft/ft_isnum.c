int				ft_isnum(char *str)
{
	int			i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '-' || str[i] == '+')
			i++;
		while (str[i] >= '0' && str[i] <= '9')
			i++;
		if (str[i] != '\0')
			return (1);
	}
	return (0);
}
