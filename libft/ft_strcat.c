char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (s1[i] != '\0')
		i++;
	k = i;
	i = 0;
	while (s2[i] != '\0')
	{
		s1[k] = s2[i];
		k++;
		i++;
	}
	s1[k] = '\0';
	return (s1);
}
